<script>
import { router } from '@inertiajs/vue3';
import { onMounted, ref, watch } from 'vue';
import { initFlowbite } from 'flowbite'
export default {
    props: {
        data: Object,
        url: String
    },
    
    mounted() {
        initFlowbite();
    },

    setup() {
        let idSelect;

        const remove = (id) => {
            idSelect = id
        }

        const removeConfirm = () => {
            router.delete(`/data/${idSelect}`)
        }

        const edit = (id) => {
            router.get(`/data/${id}/edit`)
        }

        const show = (id) => {
            router.get(`/data/${id}`)
        }

        let search = ref("")

        watch(search, (value) => {
            router.get('/', { search: value }, { preserveState: true })
        })

        return {
            remove,
            removeConfirm,  
            edit,
            show,
            onMounted,
            search,
            watch
        }
    }
}
</script>
